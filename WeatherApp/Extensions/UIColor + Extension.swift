    }
    
    static func mainCellsColor() -> UIColor {
        return #colorLiteral(red: 0.09410681576, green: 0.09412374347, blue: 0.09410110861, alpha: 1)
    }
    
    static func mainTextColor() -> UIColor {
        return #colorLiteral(red: 0.6893828511, green: 0.6941673756, blue: 0.7027272582, alpha: 1)
    }
    
    static func mainTabColor() -> UIColor {
        return #colorLiteral(red: 0.1717679203, green: 0.1764954925, blue: 0.1851210892, alpha: 1)
    }
}
